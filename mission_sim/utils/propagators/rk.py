@njit
def _norm(x):
    """无穷范数（用于误差监控）。"""
    return np.max(np.abs(x))

@njit
def _step_accepted(err, rtol, atol):
    return err <= 1.0

@njit
def _compute_new_h(h, err, power, fac_min=0.2, fac_max=10.0):
    """PI 式步长控制。"""
    fac = 0.9 * err ** (-power)
    fac = min(fac_max, max(fac_min, fac))
    return h * fac

