    def _calculate_capability_scores(self) -> Dict[str, float]:
        """Calculate capability scores for different aspects"""
        info = self.report.device_info
        
        # Memory capability score (0-1)
        # 修复：检查全局内存大小是否为0
        if info.global_mem_size <= 0:
            # 如果全局内存大小为0，使用默认值4GB
            self._print("Warning: global_mem_size is 0 or negative, using default 4GB for memory score", "WARN")
            mem_score = 0.5  # 中等内存评分
        else:
            mem_score = min(1.0, info.global_mem_size / (4 * 1024**3))  # Normalize to 4GB
        
        # Compute capability score
        compute_units = max(1, info.max_compute_units)  # 确保至少为1
        clock_freq = max(100, info.max_clock_frequency)  # 确保至少为100MHz
        compute_score = min(1.0, (compute_units * clock_freq) / (2048 * 1000))  # Normalize to 2048 CUs at 1GHz
        
        # Bandwidth score (if available)
        bw_score = 0.5  # Default
        if self.report.bandwidth_results:
            h2d_speeds = [r.bandwidth_gbs for r in self.report.bandwidth_results if "Host to Device" in r.test_name]
            if h2d_speeds:
                avg_bw = np.mean(h2d_speeds)
                bw_score = min(1.0, avg_bw / 50.0)  # Normalize to 50 GB/s
        
        # Vectorization score
        vec_widths = [
            max(1, info.preferred_vector_width_float),  # 确保至少为1
            max(1, info.preferred_vector_width_double),
        ]
        vec_score = min(1.0, np.mean(vec_widths) / 4.0)  # Normalize to vector width 4
        
        # Precision support score
        precision_scores = {
            "fp16": 0.3 if info.fp16_support else 0.0,
            "fp32": 1.0,  # Always supported
            "fp64": 0.5 if info.fp64_support else 0.0,
        }
        
        return {
            "memory": mem_score,
            "compute": compute_score,
            "bandwidth": bw_score,
            "vectorization": vec_score,
            "precisions": precision_scores,
            "overall": (mem_score * 0.3 + compute_score * 0.4 + bw_score * 0.2 + vec_score * 0.1),
        }
    
    def _generate_trajectory_config(self, scores: Dict[str, float]) -> Optional[TestConfiguration]:
        """Generate configuration for trajectory integral benchmark"""
        info = self.report.device_info
        
        # Determine precision based on device support
        precision = "fp32"  # Default
        if scores["precisions"]["fp64"] > 0.5:
            precision = "fp64"
        
        # Calculate parameters based on capability scores
        compute_score = scores["compute"]
        mem_score = scores["memory"]
        
        # Base parameters (for score = 0.5)
        base_steps = 5000
        base_paths = 500
        
        # Scale parameters based on capability
        scale_factor = compute_score * 2.0  # 0-2 range
        steps = int(base_steps * scale_factor)
        paths = int(base_paths * scale_factor)
        
        # Apply memory constraints
        # Estimated memory usage: positions (paths * 4 * precision_size)
        precision_size = 4 if precision == "fp32" else 8
        estimated_memory = paths * 4 * precision_size  # bytes
        max_memory = info.global_mem_size * self.config_params["memory_safety_factor"]
        
        # 修复：检查 max_memory 是否为0
        if max_memory <= 0:
            # 如果最大内存为0或负数，使用最小安全值
            max_memory = 100 * 1024 * 1024  # 100 MB 作为最小安全值
            self._print(f"  Warning: max_memory is 0 or negative, using {max_memory/1024/1024:.0f} MB as minimum", "WARN")
        
        if estimated_memory > max_memory:
            # Reduce paths to fit in memory
            paths = int(max_memory / (4 * precision_size))
            self._print(f"  Reduced trajectory paths to {paths} due to memory constraints", "WARN")
        
        # Ensure reasonable minimums
        steps = max(1000, steps)
        paths = max(100, paths)
        
        # Limit maximums for reasonable runtime
        steps = min(50000, steps)
        paths = min(5000, paths)
        
        # Estimate runtime (very rough estimation)
        # Assuming each path-step takes about 100 operations
        total_ops = steps * paths * 100
        # Very rough ops/sec estimation based on compute units and frequency
        ops_per_sec = info.max_compute_units * info.max_clock_frequency * 1e6 * 0.1  # 10% efficiency
        estimated_runtime = total_ops / ops_per_sec if ops_per_sec > 0 else 30.0
        
        # Adjust to target runtime
        target_runtime = self.config_params["target_runtime_per_test"]
        if estimated_runtime > target_runtime * 2:
            # Reduce scale
            reduction = (target_runtime * 2) / estimated_runtime
            steps = int(steps * math.sqrt(reduction))  # sqrt to balance steps and paths
            paths = int(paths * math.sqrt(reduction))
        
        # Calculate confidence and safety
        confidence = min(1.0, compute_score * 0.8 + mem_score * 0.2)
        
        # 修复：避免除零错误
        if max_memory <= 0:
            safety = 0.0
        else:
            safety = min(1.0, 1.0 - (estimated_memory / max_memory))
        
        config = TestConfiguration(
            benchmark_type="trajectory",
            precision=precision,
            recommended_params={
                "steps": steps,
                "paths": paths,
                "iterations": self._calculate_iterations(confidence),
                "warmup": self.config_params["warmup_iterations"],
            },
            estimated_runtime=estimated_runtime,
            confidence_level=confidence,
            safety_factor=safety,
            notes=f"Based on compute score: {compute_score:.2f}, memory score: {mem_score:.2f}"
        )
        
        return config
    
    def _generate_monte_carlo_config(self, scores: Dict[str, float]) -> Optional[TestConfiguration]:
        """Generate configuration for Monte Carlo benchmark"""
        info = self.report.device_info
        
        # Determine precision
        precision = "fp32"
        if scores["precisions"]["fp64"] > 0.5:
            precision = "fp64"
        
        # Monte Carlo is compute-bound, use compute score
        compute_score = scores["compute"]
        bw_score = scores["bandwidth"]
        
        # Base samples for score = 0.5
        base_samples = 5_000_000
        
        # Scale based on capability
        scale_factor = compute_score * 2.0
        samples = int(base_samples * scale_factor)
        
        # Apply vectorization optimization
        vec_score = scores["vectorization"]
        if vec_score > 0.5:
            # Round to nearest multiple of 4 for better vectorization
            samples = ((samples + 3) // 4) * 4
        
        # Memory usage is minimal, but check anyway
        precision_size = 4 if precision == "fp32" else 8
        estimated_memory = samples * 2 * precision_size  # x and y coordinates
        max_memory = info.global_mem_size * self.config_params["memory_safety_factor"]
        
        if estimated_memory > max_memory:
            samples = int(max_memory / (2 * precision_size))
            self._print(f"  Reduced Monte Carlo samples to {samples} due to memory constraints", "WARN")
        
        # Ensure reasonable bounds
        samples = max(1_000_000, samples)
        samples = min(100_000_000, samples)
        
        # Estimate runtime
        # Each sample: random number generation (2 values) + multiplication + addition + comparison
        total_ops = samples * 10  # ~10 ops per sample
        # Very rough ops/sec estimation based on compute units and frequency
        ops_per_sec = info.max_compute_units * info.max_clock_frequency * 1e6 * 0.15  # 15% efficiency
        estimated_runtime = total_ops / ops_per_sec if ops_per_sec > 0 else 20.0
        
        # Adjust to target runtime
        target_runtime = self.config_params["target_runtime_per_test"]
        if estimated_runtime > target_runtime * 2:
            reduction = (target_runtime * 2) / estimated_runtime
            samples = int(samples * reduction)
        
        # Calculate confidence and safety
        confidence = min(1.0, compute_score * 0.7 + bw_score * 0.3)
        safety = min(1.0, 1.0 - (estimated_memory / max_memory))
        
        # Determine batching strategy
        if samples > 10_000_000:
            batches = max(2, samples // 5_000_000)
            samples_per_batch = samples // batches
        else:
            batches = 1
            samples_per_batch = samples
        
        config = TestConfiguration(
            benchmark_type="monte_carlo",
            precision=precision,
            recommended_params={
                "samples": samples,
                "batches": batches,
                "samples_per_batch": samples_per_batch,
                "iterations": self._calculate_iterations(confidence),
                "warmup": self.config_params["warmup_iterations"],
            },
            estimated_runtime=estimated_runtime,
            confidence_level=confidence,
            safety_factor=safety,
            notes=f"Using {batches} batch(es) for memory efficiency"
        )
        
        return config
    
    def _generate_nbody_config(self, scores: Dict[str, float]) -> Optional[TestConfiguration]:
        """Generate configuration for N-body simulation benchmark"""
        info = self.report.device_info
        
        # Determine precision
        precision = "fp32"
        if scores["precisions"]["fp64"] > 0.5:
            precision = "fp64"
        
        compute_score = scores["compute"]
        mem_score = scores["memory"]
        
        # N-body is O(N²) complexity, be conservative
        # Base parameters for score = 0.5
        base_bodies = 500
        base_steps = 50
        
        # Scale based on capability (square root since complexity is O(N²))
        scale_factor = math.sqrt(compute_score * 2.0)
        bodies = int(base_bodies * scale_factor)
        steps = int(base_steps * scale_factor)
        
        # Apply memory constraints
        # Memory per body: position (4 floats), velocity (4 floats), mass (1 float)
        precision_size = 4 if precision == "fp32" else 8
        bytes_per_body = (4 + 4 + 1) * precision_size
        estimated_memory = bodies * bytes_per_body
        max_memory = info.global_mem_size * self.config_params["memory_safety_factor"]
        
        if estimated_memory > max_memory:
            bodies = int(max_memory / bytes_per_body)
            self._print(f"  Reduced N-body particles to {bodies} due to memory constraints", "WARN")
        
        # Ensure reasonable bounds
        bodies = max(100, bodies)
        bodies = min(5000, bodies)
        steps = max(20, steps)
        steps = min(200, steps)
        
        # Determine tile mode based on local memory
        local_mem_available = info.local_mem_size
        tile_mode = False
        if local_mem_available > 16 * 1024:  # At least 16KB local memory
            # Check if we can use tile mode
            # For tile mode, need local memory for tile_size * (position + mass)
            tile_size = min(128, bodies, int(math.sqrt(local_mem_available / (bytes_per_body / 2))))
            if tile_size >= 32:  # Need reasonable tile size
                tile_mode = True
        
        # Estimate runtime (O(N²) complexity)
        total_ops = bodies * bodies * steps * 20  # ~20 ops per interaction per step
        ops_per_sec = info.max_compute_units * info.max_clock_frequency * 1e6 * 0.05  # 5% efficiency for O(N²)
        estimated_runtime = total_ops / ops_per_sec if ops_per_sec > 0 else 25.0
        
        # Adjust to target runtime
        target_runtime = self.config_params["target_runtime_per_test"]
        if estimated_runtime > target_runtime * 2:
            # Reduce both bodies and steps
            reduction = math.sqrt((target_runtime * 2) / estimated_runtime)
            bodies = int(bodies * reduction)
            steps = int(steps * reduction)
        
        # Calculate confidence and safety
        confidence = min(1.0, compute_score * 0.4 + mem_score * 0.6)
        safety = min(1.0, 1.0 - (estimated_memory / max_memory))
        
        # Determine chunk size for stability
        chunk_size = 5
        if bodies > 1000:
            chunk_size = 3
        if bodies > 2000:
            chunk_size = 2
        
        config = TestConfiguration(
            benchmark_type="nbody",
            precision=precision,
            recommended_params={
                "bodies": bodies,
                "steps": steps,
                "dt": 0.01,
                "chunk_size": chunk_size,
                "tile_mode": tile_mode,
                "iterations": self._calculate_iterations(confidence),
                "warmup": self.config_params["warmup_iterations"],
            },
            estimated_runtime=estimated_runtime,
            confidence_level=confidence,
            safety_factor=safety,
            notes=f"Tile mode: {'enabled' if tile_mode else 'disabled'} (local memory: {local_mem_available/1024:.1f}KB)"
        )
        
        return config
    
    def _calculate_iterations(self, confidence: float) -> int:
        """Calculate number of iterations based on confidence level"""
        min_iter = self.config_params["min_test_iterations"]
        max_iter = self.config_params["max_test_iterations"]
        
        # Higher confidence -> fewer iterations needed
        iterations = int(min_iter + (max_iter - min_iter) * (1.0 - confidence))
        
        # Ensure odd number for better median calculation
        if iterations % 2 == 0:
            iterations += 1
        
        return max(min_iter, min(max_iter, iterations))
    
