class GPUBaseCapability:
    """GPU basic capability detector with automatic test configuration generation"""
    
    def __init__(self, platform_idx: Optional[int] = None, 
                 device_idx: Optional[int] = None,
                 verbose: bool = True):
        """
        Initialize GPU capability detector
        
        Args:
            platform_idx: OpenCL platform index, defaults to first
            device_idx: Device index, defaults to first GPU device
            verbose: Whether to print detailed information
        """
        if not PYOPENCL_AVAILABLE:
            raise RuntimeError("PyOpenCL not available")
        
        self.verbose = verbose
        self.context = None
        self.queue = None
        self.device = None
        self.platform = None
        
        # Initialize OpenCL environment
        self._init_opencl(platform_idx, device_idx)
        
        # Test results storage
        self.report = CapabilityReport()
        self.report.timestamp = time.strftime("%Y-%m-%d %H:%M:%S")
        
        # Configuration generation parameters
        self.config_params = {
            "target_runtime_per_test": 10.0,  # Target runtime per test in seconds
            "max_total_runtime": 300.0,  # Maximum total runtime in seconds
            "memory_safety_factor": 0.7,  # Use only 70% of available memory
            "confidence_threshold": 0.8,  # Minimum confidence level
            "safety_threshold": 0.7,  # Minimum safety factor
            "min_test_iterations": 3,  # Minimum number of test iterations
            "max_test_iterations": 10,  # Maximum number of test iterations
            "warmup_iterations": 2,  # Number of warmup iterations
        }
    
    def _print(self, msg: str, level: str = "INFO"):
        """Print information"""
        if self.verbose:
            timestamp = time.strftime("%H:%M:%S")
            print(f"[{timestamp}] [{level}] {msg}")
    
    def _init_opencl(self, platform_idx: Optional[int] = None, 
                    device_idx: Optional[int] = None):
        """Initialize OpenCL environment"""
        self._print("Initializing OpenCL environment...")
        
        # Get platform list
        platforms = cl.get_platforms()
        if not platforms:
            raise RuntimeError("No OpenCL platforms found")
        
        # Select platform
        if platform_idx is not None and platform_idx < len(platforms):
            self.platform = platforms[platform_idx]
        else:
            self.platform = platforms[0]
        
        self._print(f"Selected platform: {self.platform.name}")
        self._print(f"Platform vendor: {self.platform.vendor}")
        
        # Prefer GPU devices
        devices = self.platform.get_devices(cl.device_type.GPU)
        if not devices:
            devices = self.platform.get_devices(cl.device_type.ALL)
            self._print("No GPU found, falling back to any device type")
        
        if not devices:
            raise RuntimeError("No OpenCL devices found")
        
        # Select device
        if device_idx is not None and device_idx < len(devices):
            self.device = devices[device_idx]
        else:
            self.device = devices[0]
        
        # Create context and command queue
        self.context = cl.Context([self.device])
        
        # Try to enable profiling, but not mandatory
        try:
            self.queue = cl.CommandQueue(self.context, 
                                        properties=cl.command_queue_properties.PROFILING_ENABLE)
        except:
            self.queue = cl.CommandQueue(self.context)
            self._print("Profiling not available, using basic command queue")
    
    def collect_device_info(self) -> DeviceInfo:
        """Collect device basic information"""
        self._print("Collecting device information...")
        
        info = DeviceInfo()
        
        # Basic information
        info.name = self.device.name
        info.vendor = self.device.vendor
        info.version = self.device.version
        info.driver_version = self.device.driver_version
        info.device_type = str(self.device.type)
        info.platform_name = self.platform.name
        info.platform_vendor = self.platform.vendor
        
        # Compute resources - 添加安全检查
        info.max_compute_units = max(1, self.device.max_compute_units)
        info.max_work_group_size = max(1, self.device.max_work_group_size)
        info.max_work_item_dimensions = max(1, self.device.max_work_item_dimensions)
        
        # Get max work item sizes for each dimension
        max_sizes = []
        for i in range(info.max_work_item_dimensions):
            max_sizes.append(max(1, self.device.max_work_item_sizes[i]))
        info.max_work_item_sizes = max_sizes
        
        # Vector widths - 确保至少为1
        info.preferred_vector_width_char = max(1, self.device.preferred_vector_width_char)
        info.preferred_vector_width_short = max(1, self.device.preferred_vector_width_short)
        info.preferred_vector_width_int = max(1, self.device.preferred_vector_width_int)
        info.preferred_vector_width_long = max(1, self.device.preferred_vector_width_long)
        info.preferred_vector_width_float = max(1, self.device.preferred_vector_width_float)
        info.preferred_vector_width_double = max(1, self.device.preferred_vector_width_double)
        info.native_vector_width_char = max(1, self.device.native_vector_width_char)
        info.native_vector_width_short = max(1, self.device.native_vector_width_short)
        info.native_vector_width_int = max(1, self.device.native_vector_width_int)
        info.native_vector_width_long = max(1, self.device.native_vector_width_long)
        info.native_vector_width_float = max(1, self.device.native_vector_width_float)
        info.native_vector_width_double = max(1, self.device.native_vector_width_double)
        
        # Work group size multiple - 确保至少为1
        try:
            info.preferred_work_group_size_multiple = max(1, self.device.preferred_work_group_size_multiple)
        except:
            info.preferred_work_group_size_multiple = 1
            self._print("Preferred work group size multiple not available, using 1", "WARN")
        
        # Memory architecture - 添加最小安全值
        info.global_mem_size = max(100 * 1024 * 1024, self.device.global_mem_size)  # 至少100MB
        info.global_mem_cache_size = max(0, self.device.global_mem_cache_size)
        info.global_mem_cacheline_size = max(64, self.device.global_mem_cacheline_size)  # 至少64字节
        info.local_mem_size = max(1024, self.device.local_mem_size)  # 至少1KB
        info.local_mem_type = str(self.device.local_mem_type)
        info.constant_buffer_size = max(1024, self.device.max_constant_buffer_size)  # 至少1KB
        info.max_mem_alloc_size = max(10 * 1024 * 1024, self.device.max_mem_alloc_size)  # 至少10MB
        info.max_parameter_size = max(1024, self.device.max_parameter_size)  # 至少1KB
        
        # Clock and frequency - 确保合理值
        info.max_clock_frequency = max(100, self.device.max_clock_frequency)  # 至少100MHz
        info.profiling_timer_resolution = max(1, self.device.profiling_timer_resolution)
        
        # Extension support
        info.extensions = self.device.extensions.strip().split()
        
        # Check precision support
        info.fp16_support = 'cl_khr_fp16' in info.extensions
        info.fp64_support = 'cl_khr_fp64' in info.extensions or 'cl_amd_fp64' in info.extensions
        
        # Check 64-bit integer support
        info.int64_support = 'cl_khr_int64' in info.extensions
        
        # Execution capabilities
        cmd_queue_props = []
        props = self.device.queue_properties
        if props & cl.command_queue_properties.OUT_OF_ORDER_EXEC_MODE_ENABLE:
            cmd_queue_props.append("OUT_OF_ORDER_EXEC_MODE_ENABLE")
        if props & cl.command_queue_properties.PROFILING_ENABLE:
            cmd_queue_props.append("PROFILING_ENABLE")
        info.command_queue_properties = cmd_queue_props
        
        info.error_correction_support = self.device.error_correction_support
        info.endian_little = self.device.endian_little
        info.available = self.device.available
        info.compiler_available = self.device.compiler_available
        info.linker_available = self.device.linker_available
        
        # Check unified memory
        try:
            unified = self.device.host_unified_memory
            info.unified_memory = unified
        except:
            info.unified_memory = False
        
        self.report.device_info = info
        return info
    
    def test_precision_support(self) -> Dict[str, bool]:
        """Test various numerical precision support"""
        self._print("Testing numerical precision support...")
        
        precision_support = {}
        
        # Basic precision support
        precision_support["fp16"] = self.report.device_info.fp16_support
        precision_support["fp32"] = True  # All OpenCL devices support fp32
        precision_support["fp64"] = self.report.device_info.fp64_support
        
        # Integer precision support
        precision_support["int8"] = True  # Usually supported
        precision_support["int16"] = True
        precision_support["int32"] = True
        precision_support["int64"] = self.report.device_info.int64_support
        
        # Vector type support
        extensions = self.report.device_info.extensions
        precision_support["float3"] = True
        precision_support["float4"] = True
        precision_support["float8"] = True
        precision_support["float16"] = True
        
        # Check double precision vector support
        if precision_support["fp64"]:
            precision_support["double2"] = True
            precision_support["double4"] = True
            precision_support["double8"] = True
            precision_support["double16"] = True
        else:
            precision_support["double2"] = False
            precision_support["double4"] = False
            precision_support["double8"] = False
            precision_support["double16"] = False
        
        # Special extensions
        precision_support["half"] = precision_support["fp16"]
        precision_support["cl_amd_media_ops"] = 'cl_amd_media_ops' in extensions
        precision_support["cl_amd_media_ops2"] = 'cl_amd_media_ops2' in extensions
        
        self.report.precision_support = precision_support
        return precision_support
    
    def test_memory_bandwidth(self, test_sizes: List[int] = None) -> List[BandwidthResult]:
        """
        Test memory bandwidth
        
        Args:
            test_sizes: List of test data sizes (bytes)
        """
        self._print("Testing memory bandwidth...")
        
        if test_sizes is None:
            # Default test sizes: 1KB, 1MB, 10MB, 100MB
            test_sizes = [1024, 1024*1024, 10*1024*1024, 100*1024*1024]
            # Adjust test sizes to not exceed half of device max allocation memory
            max_size = self.report.device_info.max_mem_alloc_size // 2
            test_sizes = [size for size in test_sizes if size <= max_size]
            if not test_sizes:
                test_sizes = [max_size // 4]  # Use 1/4 of max allocation memory
        
        results = []
        
        # Test 1: Host to device transfer bandwidth
        for size in test_sizes:
            self._print(f"Testing H2D bandwidth with {size/1024/1024:.2f} MB data...")
            
            # Prepare test data
            host_data = np.random.rand(size // 4).astype(np.float32)  # 4 bytes per element
            
            # Warmup
            for _ in range(2):
                dev_data = cl_array.to_device(self.queue, host_data[:100])
                self.queue.finish()
            
            # Actual test
            start_time = time.perf_counter()
            
            try:
                dev_data = cl_array.to_device(self.queue, host_data)
                self.queue.finish()
            except Exception as e:
                self._print(f"  Error: {e}", "WARN")
                continue
            
            end_time = time.perf_counter()
            transfer_time = end_time - start_time
            
            # Calculate bandwidth (GB/s)
            bandwidth = (size / (1024**3)) / transfer_time
            
            results.append(BandwidthResult(
                test_name="Host to Device",
                transfer_size_bytes=size,
                transfer_time_ms=transfer_time * 1000,
                bandwidth_gbs=bandwidth,
                operations_per_second=1.0 / transfer_time,
                notes=f"Data size: {size/1024/1024:.2f} MB"
            ))
        
        # Test 2: Device to host transfer bandwidth
        for size in test_sizes:
            self._print(f"Testing D2H bandwidth with {size/1024/1024:.2f} MB data...")
            
            # Prepare device data
            host_data = np.random.rand(size // 4).astype(np.float32)
            dev_data = cl_array.to_device(self.queue, host_data)
            self.queue.finish()
            
            # Warmup
            for _ in range(2):
                small_result = dev_data[:100].get()
            
            # Actual test
            start_time = time.perf_counter()
            
            try:
                result = dev_data.get()
                self.queue.finish()
            except Exception as e:
                self._print(f"  Error: {e}", "WARN")
                continue
            
            end_time = time.perf_counter()
            transfer_time = end_time - start_time
            
            # Verify data
            if not np.allclose(result, host_data, rtol=1e-5):
                self._print(f"  Warning: Data verification failed", "WARN")
            
            # Calculate bandwidth
            bandwidth = (size / (1024**3)) / transfer_time
            
            results.append(BandwidthResult(
                test_name="Device to Host",
                transfer_size_bytes=size,
                transfer_time_ms=transfer_time * 1000,
                bandwidth_gbs=bandwidth,
                operations_per_second=1.0 / transfer_time,
                notes=f"Data size: {size/1024/1024:.2f} MB"
            ))
        
        # Test 3: Device internal copy bandwidth
        for size in test_sizes[:2]:  # Only test smaller two sizes
            self._print(f"Testing Device Copy bandwidth with {size/1024/1024:.2f} MB data...")
            
            # Create kernel for device internal copy
            kernel_code = """
            __kernel void copy_buffer(__global float* src, __global float* dst, int n) {
                int idx = get_global_id(0);
                if (idx < n) {
                    dst[idx] = src[idx];
                }
            }
            """
            
            # Prepare data
            n_elements = size // 4
            host_src = np.random.rand(n_elements).astype(np.float32)
            
            # Create buffers
            mf = cl.mem_flags
            src_buf = cl.Buffer(self.context, mf.READ_ONLY | mf.COPY_HOST_PTR, hostbuf=host_src)
            dst_buf = cl.Buffer(self.context, mf.WRITE_ONLY, size=size)
            
            # Compile kernel
            program = cl.Program(self.context, kernel_code).build()
            kernel = program.copy_buffer
            
            # Set work group size
            max_wg = self.report.device_info.max_work_group_size
            local_size = min(256, max_wg, n_elements)
            global_size = ((n_elements + local_size - 1) // local_size) * local_size
            
            # Warmup
            for _ in range(2):
                kernel(self.queue, (global_size,), (local_size,), src_buf, dst_buf, np.int32(n_elements))
                self.queue.finish()
            
            # Actual test
            start_time = time.perf_counter()
            
            # Execute multiple copies for more accurate timing
            iterations = 10
            for _ in range(iterations):
                kernel(self.queue, (global_size,), (local_size,), src_buf, dst_buf, np.int32(n_elements))
            self.queue.finish()
            
            end_time = time.perf_counter()
            total_time = end_time - start_time
            avg_time = total_time / iterations
            
            # Calculate bandwidth (consider each iteration copies data)
            bandwidth = (size / (1024**3)) / avg_time
            
            results.append(BandwidthResult(
                test_name="Device Copy",
                transfer_size_bytes=size,
                transfer_time_ms=avg_time * 1000,
                bandwidth_gbs=bandwidth,
                operations_per_second=1.0 / avg_time,
                notes=f"Data size: {size/1024/1024:.2f} MB, Iterations: {iterations}"
            ))
        
        self.report.bandwidth_results = results
        return results
    
