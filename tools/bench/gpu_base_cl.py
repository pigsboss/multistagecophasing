    def run_all_tests(self):
        """Run all tests"""
        start_time = time.perf_counter()
        
        # Collect device information
        self.collect_device_info()
        
        # Test precision support
        self.test_precision_support()
        
        # Test constraints
        self.test_constraints()
        
        # Test bandwidth (using safe test sizes)
        max_test_size = min(100 * 1024 * 1024, self.report.device_info.max_mem_alloc_size // 4)
        if max_test_size > 1024:  # At least 1KB
            test_sizes = [
                1024,  # 1KB
                1024 * 1024,  # 1MB
                min(10 * 1024 * 1024, max_test_size),  # 10MB or smaller
                min(100 * 1024 * 1024, max_test_size)  # 100MB or smaller
            ]
            # Remove duplicates
            test_sizes = list(dict.fromkeys(test_sizes))
            self.test_memory_bandwidth(test_sizes)
        
        # Generate recommended settings
        self.generate_recommendations()
        
        end_time = time.perf_counter()
        self.report.test_duration_seconds = end_time - start_time
        
        return self.report
    
