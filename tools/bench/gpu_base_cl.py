    platform_vendor: str = ""
    
    # Compute resources
    max_compute_units: int = 0
    max_work_group_size: int = 0
    max_work_item_dimensions: int = 0
    max_work_item_sizes: List[int] = field(default_factory=list)
    preferred_vector_width_char: int = 0
    preferred_vector_width_short: int = 0
    preferred_vector_width_int: int = 0
    preferred_vector_width_long: int = 0
    preferred_vector_width_float: int = 0
    preferred_vector_width_double: int = 0
    preferred_work_group_size_multiple: int = 0
    native_vector_width_char: int = 0
    native_vector_width_short: int = 0
    native_vector_width_int: int = 0
    native_vector_width_long: int = 0
    native_vector_width_float: int = 0
    native_vector_width_double: int = 0
    
    # Memory architecture
    global_mem_size: int = 0  # bytes
    global_mem_cache_size: int = 0  # bytes
    global_mem_cache_type: str = ""
    global_mem_cacheline_size: int = 0  # bytes
    local_mem_size: int = 0  # bytes
    local_mem_type: str = ""
    constant_buffer_size: int = 0  # bytes
    max_mem_alloc_size: int = 0  # bytes
    max_parameter_size: int = 0  # bytes
    
    # Clock and frequency
    max_clock_frequency: int = 0  # MHz
    profiling_timer_resolution: int = 0  # ns
    
    # Extension support
    extensions: List[str] = field(default_factory=list)
    fp16_support: bool = False
    fp64_support: bool = False
    int64_support: bool = False
    
    # Execution capabilities
